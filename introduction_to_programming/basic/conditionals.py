# Пятое задание:
number = input("Введите число, кратное '3' и заканчивающееся на '5': ")
if number.replace("-", "").isdigit():
    if int(number) % 3 == 0:
        if number.endswith("5"):
            print("Введённое Вами число кратно '3' и заканчивается цифрой '5'.")
        elif not number.endswith("5"):
            print("Введённое Вами число кратно '3', но не заканчивается цифрой '5'.")
    if int(number) % 3 != 0:
        if number.endswith("5"):
            print("Введённое Вами число не кратно '3', но заканчивается цифрой '5'.")
        elif not number.endswith("5"):
            print("Введённое Вами число не кратно '3' и не заканчивается цифрой '5'.")
else:
    print("Было введено некорректное число.")

# Шестое задание:
while True:
    try:
        first_number = float(input("Введите первое число: "))
        second_number = float(input("Введите второе число: "))
        if first_number == second_number:
            print("Введённые числа равны между собой.")
            break
        elif first_number < second_number:
            print("Первое число меньше второго числа.")
            break
        elif first_number > second_number:
            print("Первое число больше второго числа.")
            break
    except ValueError:
        print("Введено некорректное число.")

# Седьмое задание, первый вариант:
first_number = input("Введите первое целое число: ")
second_number = input("Введите второе целое число: ")
third_number = input("Введите третье целое число: ")
if first_number.isdigit() and second_number.isdigit() and third_number.isdigit():
    count_number = 0
    if int(first_number) % 2 == 0:
        count_number += 1
    if int(second_number) % 2 == 0:
        count_number += 1
    if int(third_number) % 2 == 0:
        count_number += 1
    print(f"Количество чётных чисел из введённых Вами чисел равно {count_number}.")
else:
    print("Было введено некорректное число.")

# Седьмое задание, второй вариант:
numbers = input("Введите три целых числа, разделённых пробелами: ").split()
if len(numbers) == 3 and all(num.isdigit() for num in numbers):
    count_even = sum(1 for num in numbers if int(num) % 2 == 0)
    print(f"Количество чётных чисел из введённых Вами чисел равно {count_even}.")
else:
    print("Были введены некорректные числа.")

# Восьмое задание:
