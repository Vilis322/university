# Двенадцатое задание:
while True:
    first_string = input("""Введите два целых положительных числа через пробел, вторым числом не должен быть '0'.
Или введите 'выход' без кавычек для выхода из программы: """)
    if first_string == 'выход':
        break
    numbers = first_string.split()
    if len(numbers) == 2:
        a, b = map(int, numbers)
        if b != 0:
            print("""Выберите арифметическую операцию для двух введённых чисел, обозначенную числами ниже:
                   1 - сложение;
                   2 - вычитание;
                   3 - умножение;
                   4 - деление.""")
            while True:
                second_string = int(input("Укажите выбранную операцию одним из символов, приведённых выше, "
                                          "без пробелов: "))
                declension_dict = {1: "сложения", 2: "вычитания", 3: "умножения", 4: "деления"}
                operations_dict = {1: lambda x, y: x + y,
                                   2: lambda x, y: x - y,
                                   3: lambda x, y: x * y,
                                   4: lambda x, y: x / y}
                if second_string in operations_dict:
                    result = operations_dict[second_string](a, b)
                    print(f"Вы выбрали операцию {declension_dict[second_string]} для введённых Вами чисел,"
                          f" вот результат этой операции: ")
                    print(result)
                    break
                else:
                    print("Номер введённой операции некорректен. "
                          "Пожалуйста, введите ещё раз корректно номер операции.")
        else:
            print("Второе число не может быть равно 0. Пожалуйста, введите корректные числа.")
    else:
        print("Введённая строка не является двумя числами, введёнными через пробел. "
              "Пожалуйста, введите корректные числа.")
